      <h2><span class="sec-num">09</span>Refund and Withdrawal Policy</h2>

      <h3>Can I Withdraw Money from My Span HSA?</h3>
      <p>No. Your Span Health Savings Account is a purpose-restricted wallet designated exclusively for healthcare
        services and Sharia-compliant ethical investment participation. Cash withdrawals to personal bank
        accounts are not permitted under the Span HSA model.</p>

      <h3>What Happens to My Balance If I Close My Account?</h3>
      <p>When you close your account you have 90 days to choose what happens to your remaining balance:</p>
      <ul>
        <li><strong>Use it for healthcare</strong> — transfer your balance directly to an NHIA-licensed HMO,
          hospital, or pharmacy of your choice</li>
        <li><strong>Transfer to a family member</strong> — send it to a dependent's Span HSA or to another
          registered Span member</li>
        <li><strong>Donate for good</strong> — donate it to the Span Waqf and Charity Healthcare Fund to support
          healthcare access for those in need</li>
      </ul>

      <h3>Exceptional Cash Refund Cases</h3>
      <p>Cash refunds to your bank account are only processed in the following exceptional circumstances:</p>
      <ul>
        <li>Duplicate payments or verified system errors</li>
        <li>Prohibited deposits made in error and flagged within 48 hours</li>
        <li>A regulatory requirement issued by the CBN, SEC, NDPC, or a valid court order</li>
      </ul>
      <p>All exceptional refunds are subject to verification and may take up to 30 business days to process.</p>

      <div class="highlight-box">
        <p><strong style="color:#0a1f35;font-family:'Inter',sans-serif">Important Notes</strong><br>
          A small processing fee may apply upon account closure. Any investment losses incurred through the
          ethical investment pool will be deducted before your balance is transferred. Accounts inactive for
          36 consecutive months will have their balances transferred to the Span Waqf and Charity Healthcare
          Fund in accordance with Islamic dormancy principles.</p>
      </div>
